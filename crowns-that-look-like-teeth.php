				<p style="text-align: left;">A dental crown is a sort of cap that is placed on the tooth in order to replace the damaged enamel and to protect the dental pulp and nerve inside. It can be put in for various reasons: a broken tooth, decayed tooth, cosmetic reasons etc. Crowns that look like teeth are made of a variety of materials depending on their use and we at The Dental Hub cater to a patients specific needs. </p>
<p style="text-align: left;"><b>For individuals who want cosmetically viable crowns that look exactly like their other teeth, we recommend the following types of crowns:</b></p>
<p style="text-align: left;">- All resin crowns are more prone to chipping and breakage but are much less expensive than porcelain fused to metal crowns.</p>
<p style="text-align: left;">- All ceramic/porcelain crowns are best for those with allergies to metal and can be affixed on any teeth- front or back.</p>
<p style="text-align: left;">To get a dental crown fitted, the patient may have to come into our clinic for two or more sittings depending on the issue at hand. In the first sitting, dental x-rays are made and the tooth or teeth to receive crowns are filed down or built up (using filling) to get to the desired shape and size for them to be ready to receive the crown. In the next sitting the crowns that have been made from the impressions are affixed onto the tooth. Subsequently the bite and shape adjustments are made.</p>
<p style="text-align: left;"><b>Some of the problems that may arise following putting in a crown may be:</b></p>
<p style="text-align: left;">- Experiencing some heat and cold sensitivity is common. Pain or sensitivity that occurs when biting down usually means that the crown is too high on the tooth but this is a problem easily remedied by the dentist.</p>
<p style="text-align: left;">- Crowns made of  full porcelain can sometimes chip. If the chip is small, a composite resin can be used to repair the chip which is just a temporary fix. If the chipping is extensive, the crown may need to be replaced.</p>
<p style="text-align: left;">- Sometimes the cement that holds the crown in place washes off. This not only loosens the crown but also causes bacteria to enter and cause more decay to the tooth. If this problem occurs, a visit to the dentist is a must.</p>
