<p style="text-align: left;">- Traditional dental bridge: These are the most popular variety of dental bridges and they comprise of one or more fake teeth held in place with dental crowns. These are placed in a gap between natural teeth and the crowns are fitted onto the natural teeth on either side of the gap.</p>
<p style="text-align: left;">- Cantilever bridge: This kind of dental bridge is secured by affixing the dental crown on the natural tooth on one side of the gap that is being filled. The enamel of the natural tooth will be filed and the new dental crown will be fixed on it to secure the bridge into place.</p>
<p style="text-align: left;">- Maryland bridge: Instead of dental crowns holding the fake teeth together as in traditional bridges, the fake teeth in this kind of bridging is held together by a metal or porcelain framework that are bonded behind the two adjacent teeth.</p>
