				<p style="text-align: left;">Tooth dislodgement is called dental avulsion. Whatever is the cause, be it due to contact sports or accidental injury having a partially dislodged tooth can be an uncomfortable and often painful prospect. A tooth maybe just hanging from the socket (bone) or it may be just attached at the nerve or root.</p>
