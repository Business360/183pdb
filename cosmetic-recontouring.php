				<p style="text-align: left;">Odontoplasty or enameloplasty are the technical names for re-contouring of teeth. As a part of these procedures, a tooths length, shape or surface is changed by removing the enamel for cosmetic reasons. This is considered to be one of the easiest, pain-free and quick procedures to correct a smile and the results are immediate.</p>
<p style="text-align: left;">Recontouring is a useful method to correct minor imperfections, such as</p>
<p style="text-align: left;">- Fixing small chips in the tooth</p>
<p style="text-align: left;">- Smoothening out ridges or furrows in the enamel of the tooth</p>
<p style="text-align: left;">- Adjusting minor irregularities in tooth shape caused by crowded or uneven teeth</p>
<p style="text-align: left;">- Adjusting the length of the canines to make them conform to the size of the rest of the teeth. </p>
<p style="text-align: left;">Recontouring can also prove beneficial to dental health by removing gaps or overlaps between teeth in which plaque or food debris can accumulate. It is important to note than in case of a broken or chipped tooth, recontouring may not be the solution. Our dentists at The Dental Hub will be happy to discuss which of your teeth can be perfected by just recontouring and which will require more work like fillings or crowns etc.</p>
<p style="text-align: left;">To make sure if a candidate is suited for recontouring, the dentist will first take an X-ray of his/her teeth to determine the size and location of the pulp (innermost layer that houses the nerve). If the enamel layer is too thin or if the pulp lies too close to the tooth's surface, recontouring may not be possible and some other procedure such as bonding or veneers may be more appropriate. </p>
<p style="text-align: left;">At the sitting for the recontouring, generally anaesthesia is not required as it is just the tooth enamel that is being worked upon. The dentist may use implements like sandpaper and a diamond bur to smoothen the imperfections on the tooth surface. The edges of the teeth are sandpapered to achieve the desired finish. This is the shaping process. Once this is complete, the polishing equipment will be used to clean and polish the teeth. </p>
<p style="text-align: left;">There are no special follow-up procedures required for a simple tooth recontouring however if the enamel is worn too thin, then sensitivity may occur which needs follow-up. </p>
