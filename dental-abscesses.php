				</h2>
				<p style="text-align: left;">A pocket of pus that forms around the root of the tooth due to infection is called an abscess. These can be painful and cause a lot of discomfort if not given the necessary dental attention. An oral abscess may be of three different kinds:</p>
<p style="text-align: left;">- A periodontal abscess forms in the space between a tooth and the gum. The cause is generally poor dental hygiene and food debris rotting in these spaces causing bacterial growth. Such abscesses are difficult to deal with as there is little or no space for the pus in the abscess to drain and chances for infection of teeth, bone and gums are high.</p>
<p style="text-align: left;">- A gingival abscess forms on the gum surface, often on the area where the crown of the tooth meets the gum line. The cause for these abscesses is almost always external damage like the tine of a fork or a toothpick poking that area. If left untreated, gingival abscesses can grow into periodontal abscesses.</p>
<p style="text-align: left;">- A periapical abscess generally occurs in teeth with a large decay. In this case the dental pulp develops the infection (known as pulpitis) which can spread outwards to destroy the enamel and inwards to damage the nerve and root. It may also show up on the gum line or cause pain in the area surrounding the affected tooth. </p>
<p style="text-align: left;">At The Dental Hub, our dentists provide emergency support for patients with abscessed teeth and gums. The pain can be so much that it may cause facial swelling, prevents chewing and swallowing and in some cases even results in a fever from the infection. </p>
<p style="text-align: left;">The treatment priority is to drain the pus and treat the infected area to prevent further spreading of the infection. First off, the dentist may rely on dental X-rays to determine the nature and extent of the abscess. In case of minor abscesses manual pressure is enough to release the pus but in other more complicated abscesses the dental surgeon might need to make a cut on the gum to create space for the pus to drain.</p>
