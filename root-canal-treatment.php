				</h2>
				<p style="text-align: left;">The root canal is the chamber inside the tooth that contains the dental nerve. Sometimes due to infections or decay, a tooth becomes badly damaged. If a tooth nerve is exposed, root canal treatment is necessary, which is performed by removing the dental nerve (pulp) from that specific tooth and the tooth is resealed with a filling. This is done to prevent the infection or decay to spread from one tooth to another and to prevent the growth of abscesses. </p>
<p style="text-align: left;">The main cause for a root canal procedure is the spread of infection inside the root canal chamber. Sometimes just the pulp and sometimes the whole tooth including the nerve get infected and bacteria begin to grow inside this chamber. In some cases, the bacterial infection causes a painful pus-filled abscess near the tooth and there are chances that if left untreated, the bacterial damage will get to other teeth.</p>
<p style="text-align: left;">A dentist or a specialist endodontist performs the root canal surgery. The choice of which kind of dental health practitioner to use comes from the careful examination of the condition of the teeth and the degree of precision required in the surgery. </p>
<p style="text-align: left;"><b>Steps in root canal treatment:</b></p>
<p style="text-align: left;">1 A dental X-Ray is done to ascertain the degree and extent of damage- whether or not more than one teeth are affected, how badly the infection has spread etc. are determined from this.</p>
<p style="text-align: left;">2 Generally the nerve in the decayed tooth is dead but to make the procedure as pain free and comfortable as possible for the patient, a local anaesthetic is applied to the area.</p>
<p style="text-align: left;">3 To keep the area dry and free from saliva, a rubber dam is placed around the area of operation.</p>
<p style="text-align: left;">4 Root canal files are used to clean out the debris, infected pulp and the dead nerve.</p>
<p style="text-align: left;">5 The sides of the root canal are scraped out and the area is washed with water to clear out any remaining debris.</p>
<p style="text-align: left;">6 The next step is to seal the tooth. Whether or not this is done on the same day as the drilling and scraping is determined by the extent of the infection and damage. In severe cases, the dentist applies some medication inside the root canal, puts in a temporary filling and asks the patient to come back in a weeks time for the next phase.</p>
<p style="text-align: left;">7 A sealer paste and rubber compound are generally what the root canal cavity is filled with and the exterior hole is filled in with a composite filling to match the colour of the teeth.</p>
<p style="text-align: left;">8 Some restoration work maybe required after the root canal procedure to ensure the health of the teeth which will be discussed with the patient by the dentist.</p>
<p style="text-align: left;">Some sensitivity is to be expected immediately after a root canal procedure that can be addressed with over the counter medication.</p>
