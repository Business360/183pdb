				</h2>
				<p style="text-align: left;">A dental cavity is repaired using filling material that is closest in shade to the patients tooth but as with normal teeth, sometimes, the filling material erodes unevenly and accumulates stains more easily than the rest of the tooth enamel. This is when the filling area appears discoloured and shows up as unsightly marks on the teeth. </p>
<p style="text-align: left;">To explain in greater detail, the composite (tooth coloured) filling materials are made partly of plastic resin and this resin can become porous on the surface picking up stains or appearing more yellow. The good news is that it is just the surface layer of the filling, which is affected by the discolouration, and if a few microns of it are removed then the area appears normal and uniform in colour. Opting for replacement of just the surface layers of the filling gives the tooth a new lease of life, which is advisable over repeated re-fillings, which result in dental decay and eventually a root canal has to be performed on the tooth. </p>
<p style="text-align: left;">Our dentists at The Dental Hub always recommend that those patients who have had their teeth filled should practice good oral hygiene in order to keep the discolouration of their fillings at bay. A solid regimen of brushing and flossing with six monthly dental check-ups is all it takes. </p>
<p style="text-align: left;">A stained restoration may or may not reveal underlying tooth decay. If the tooth is healthy and just the filling is discoloured, sometimes the dentist may just polish or buff the tooth with polishing discs to revitalise it. Another reason to replace tooth fillings is if the client has undergone a whitening procedure. This leaves the teeth looking whiter than the filled in portions, which then have to be polished or scraped and refilled to match the rest of the teeth colour. </p>
<p style="text-align: left;">White fillings can become stained particularly around the edges and this is generally treated by smoothing down the edges. In some cases, replacement is needed to get the desired result. White fillings may also change colour as time passes and hence have to be polished or treated to achieve cosmetically viable results.</p>
