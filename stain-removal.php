				<p style="text-align: left;">Dental stains can seriously impact the way a smile looks. This is the reason why a lot of our patients at The Dental Hub come to us for stain removal solutions which we provide with utmost care.</p>
<p style="text-align: left;">The first thing to know when dealing with dental stain is the type of stain and depending on this information, the course of treatment will be chosen.</p>
<p style="text-align: left;"><b>Intrinsic Teeth Stain Removal</b></p>
<p style="text-align: left;">Intrinsic stains refer to staining of the dentin which is the sensitive layer below the dental enamel. This layer becomes a darker over time, which is observable if the enamel wears away due to poor oral health. This type of staining may occur due to exposure to tetracycline antibiotics during childhood or physical trauma to the teeth.</p>
<p style="text-align: left;">These stains originate inside the teeth so they cannot be removed at home with just whitening toothpaste. Instead, at-home dental whitening kits or a professional treatment by our dentists may be able to remove these stains. The main ingredient is bleach which is meant to remove deep stains in addition to the surface ones.</p>
<p style="text-align: left;">Bonding or crowning, which are restorative methods, can camouflage even the most rigid intrinsic stains. Bonding applies composite resin,which is of the same colour as the tooth to the surface of the stained teeth and then contours it into the correct shape. Once cured, the resin is smoothed and polished. This restores the original white colour of the teeth. Crowning, on the other hand, involves placing tooth-coloured caps on top of the discoloured teeth. This hides the staining. Our dentists will help you choose which method is right for you.</p>
<p style="text-align: left;"><b>Extrinsic Teeth Stain Removal</b></p>
<p style="text-align: left;">Staining of the enamel, which is the hard surface protecting the more nervous dentin and pulp is known as extrinsic teeth staining. Foods such as curry with turmeric in it and berries with strong pigments can have a staining effect on teeth, as can common drinks like soda, coffee, tea and red wine. Tobacco in any form is another possible cause of this type of staining.</p>
<p style="text-align: left;">Because these stains are on the outer layer of your teeth, unlike intrinsic staining, you can usually remove them with whitening toothpastes. If whitening toothpastes are not helpful, dentists can perform professional dental cleaning to remove them.</p>
