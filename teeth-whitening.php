				<p style="text-align: left;">Teeth whitening services are covered under cosmetic dentistry. This is an effective way of lightening the shade of colour of the teeth without removing any of the tooth surfaces. The existing colour of the teeth will determine what the final colour will be after lightening; therefore results may vary from patient to patient.</p>
<p style="text-align: left;">Adult teeth can get stained due to a variety of reasons: smoking, tea, coffee, red-wine etc. Tartar, also known as calculus can also affect the colour of teeth. Some have cracks in the teeth in which stains accumulate causing unsightly markings on the teeth. Very few adults have a truly white set of teeth and teeth whitening can be a viable solution for those who want to improve their smile. However, teeth whitening procedure is not for everyone. Our dentists at The Dental Hub will be happy to provide a consultation to those who want such a procedure done to discuss the process and expected results.</p>
<p style="text-align: left;">In todays day and age of selfies and social media, there are a number of over-the counter toothpastes, oral gels and rinses available that claim to whiten stained or yellowed teeth. It is best to consult the dentist before starting to use any such products that one has any doubts or questions about.</p>
<p style="text-align: left;">There can be two main kinds of in-clinic whitening procedures.</p>
<p style="text-align: left;"><b>Intrinsic:</b> Whitens the inner part of the tooth that is soaked in a hydrogen peroxide solution that causes the colour to lighten. When the inner part of the tooth is whitened, the colour is reflected through the outer coating or enamel of the teeth. Since the colour actually reflects out through the enamel like a prism, the set of teeth look whiter and brighter.</p>
<p style="text-align: left;"><b>Extrinsic:</b> External staining or yellowing of the teeth is taken care of by extrinsic whitening. Generally, the dentist will use whitening toothpaste to polish and whiten the surface of the teeth.</p>
<p style="text-align: left;">A few important points to keep in mind is that teeth whitening is a temporary process and the results will fade in 6-24 months depending on how quickly ones teeth stain and their diet and maintenance. Also, some sensitivity is to be expected within 24 hours of this procedure.</p>
