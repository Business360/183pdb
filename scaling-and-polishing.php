				<p style="text-align: left;">The smile of an individual takes any interaction to the next level. Here at The Dental Hub, we take our task of scaling and polishing teeth with the utmost importance to ensure that you leave our clinic with the pearliest set of teeth possible. </p>
<p style="text-align: left;">Through years of eating different types of food and drinking beverages like tea, coffee, cola, wine etc., teeth can become discoloured. Scaling, also known as professional cleaning is a process by which the plaque build-up on the surface of the teeth is removed and the enamel is made plain by removal of any debris and detritus that deposit on the tooth surface. For certain individuals, years of plaque deposit and bacterial infestation on the teeth may start causing gum infections. Scaling is the recommended procedure to begin with for such individuals.</p>
