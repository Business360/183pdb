				<p style="text-align: left;">Dental cavities or cracked teeth call for tooth fillings. The feeling of sensitivity when eating or drinking warm or cold foods, problems in flossing, and the awareness of a chipped part on the tooth enamel - all indicate the need for cavity fillings. It should be mentioned here that these are not the only grounds where fillings might be necessary. Sometimes on inspecting a patients teeth, the dentist may find cavities which need filling.</p>
<p style="text-align: left;">In earlier days, people had their teeth filled with precious metals like gold, mercury and silver. Nowadays people mostly prefer a more natural colour of filling that will blend in with the rest of their teeth, giving a more uniform and absolutely normal look. </p>
<p style="text-align: left;"><b>A typical dental cavity filling procedure comprises the following steps:</b>
<p style="text-align: left;">- First the cavity area is determined and a local anaesthetic is injected to numb the area for the procedure</p>
<p style="text-align: left;">- Next the detritus and decay from the tooth in question is removed by the use of dental instruments. </p>
<p style="text-align: left;">- Thirdly, the composite filling material is applied on the tooth, to even out the surface and fill in the cavity. This composite material is tooth-coloured to ensure a natural match to the other teeth so that no visible anomaly is present.</p>
<p style="text-align: left;">- Lastly the dentist has the patient open and shut their jaws to correct the bite. Any excess filling material is scraped off so as to make the patient comfortable with the filled in tooth.</p>
<p style="text-align: left;">Composite resin fillings are now the most popular cavity filling material. These provide a durable solution for small to medium-sized dental cavities and can be customised to match the existing colour of the patients teeth to give a normal look and feel. </p>
<p style="text-align: left;">Composite fillings are more difficult to put in because the oral cavity (mouth) has to be dry for it to properly set in. This is more expensive than the amalgam variety and there is no guarantee that it will be as or more durable than amalgam. However, the look and feel of the filled tooth is so utterly natural that this is the cavity filler of choice.</p>
