				<p style="text-align: left;">A dental crownis a form of dental cap placed on a tooth to improve its size, shape and strength. Crowns are also put in to improve the appearance of a chipped, discoloured or worn off tooth. </p>
<p style="text-align: left;"><b>The main reasons for having dental crowns put in are as follows:</b></p>
<p style="text-align: left;">-To support a weakened tooth by holding together its cracked parts or to prevent further decay</p>
<p style="text-align: left;">- To hold in place a dental bridge</p>
<p style="text-align: left;">- To cover the surface of a tooth with a large filling in order to better its appearance</p>
<p style="text-align: left;">- To cover a dental implant</p>
<p style="text-align: left;">- To improve the look of severely misshapen or discoloured teeth</p>
<p style="text-align: left;">Crowns are made of a variety of materials depending on their use and we at The Dental Hub cater to a patients specific needs.</p>
<p style="text-align: left;">- Stainless steel crowns are temporary crowns prepared for children and are put in place till the tooth falls off on its own, usually when a permanent tooth erupts. </p>
<p style="text-align: left;">- For out of sight teeth like inner molars, metal crowns can be a cost-effective and durable option. These crowns last the longest and wear down the least.</p>
<p style="text-align: left;">- Porcelain-fused-to-metal crowns are good for front or back teeth where strength and support is of maximum importance. However, chances of the porcelain part breaking or chipping, more wear and tear for the opposing teeth and receding gums making the metal portion visible make this a less used option.</p>
<p style="text-align: left;">- All resin crowns are more prone to chipping and breakage but are much less expensive than porcelain fused to metal crowns.</p>
<p style="text-align: left;">- All ceramic/porcelain crowns are best for those with allergies to metal and can be sued on any teeth- front or back.</p>
<p style="text-align: left;">To get a dental crown fitted, the patient may have to come into our clinic for two or more sittings depending on the issue at hand. In the first sitting, dental x-rays are made and the tooth or teeth to receive crowns are filed down or built up (using filling) to get to the desired shape and size for them to be ready to receive the crown. In the next sitting the crowns that have been made from the impressions made in the first sitting are affixed onto the tooth.Bite and shape adjustments are made as required. </p>
