				<p style="text-align: left;">Mouth guards are protective devices worn in the mouth over the teeth to protect them from injuries during sports or by teeth grinding. Mouth guards, also known as mouth protectors, are used to reduce the impact of a blow to the face, minimising the risk of getting teeth broken and preventing injuries to the lips, tongue, face or jaw. Normally, they cover the upper teeth and are used as a means to protect the soft tissues of the tongue, lips and cheek lining.</p>
<p style="text-align: left;">For contact sports like boxing etc. mouth guards are extremely useful for the players but even in other sports like skating, cricket, football etc. this should be a precautionary device in a players kit. </p>
<p style="text-align: left;">There are a number of mouth guards available in the market but the best ones are custom-made for an individual by their dentist. We at The Dental Hub are committed to providing the best mouth guards fitted and strong enough to protect your mouth. </p>
<p style="text-align: left;">There are three kinds of mouth protectors that one can wear</p>
<p style="text-align: left;"><b>Stock mouth guards:</b> These are available in most sporting goods stores and are usually quite inexpensive. These can be a bit uncomfortable and bulky as they are ready-to-wear and in actuality provide very little protection. Nothing can be really done to fix their fit in the mouth and dentists never recommend these.</p>
<p style="text-align: left;"><b>Boil-and bite mouth guards:</b> These are also available in the larger sporting goods stores and are somewhat better than the stock mouth guards. They are made of a thermoplastic material which can be moulded when hot. The instructions to be followed to fit and wear them involve submerging them in boiling water and then while the material is soft, put into the mouth and with the tongue and fingers fit it into shape around the upper teeth. </p>
<p style="text-align: left;"><b>Custom-fitted mouth guards:</b> These are the best of the lot and the most expensive. This involves a dentist taking an impression of the upper teeth of the individual and then moulding a special material over that to provide maximum protection and comfort. One can easily breathe and talk through custom-fitted mouth guards.</p>
