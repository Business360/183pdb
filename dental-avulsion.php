				<p style="text-align: left;">A trauma to the face due to a sports injury or an accident etc. may cause a tooth to get broken, loosened or completely dislodged from its socket. This is known as a dental avulsion and is a common cause for seeking emergency dental support at The Dental Hub. If a dislodged tooth cannot be reattached and needs to be extracted, then the missing tooth can be replaced by various dental replacement options such as dental bridge, crowns or dental implants. Our dentists at The Dental Hub will be happy to discuss options with the patient that would help preserve their intact smile.</p>
