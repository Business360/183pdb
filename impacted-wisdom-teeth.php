				<p style="text-align: left;">Wisdom teeth are the teeth most commonly missing in adults as they have the worst time coming out normally and hence have to be extracted. It is not uncommon to hear the dentist say that the wisdom tooth does not have the space in the gum line to sprout out to its full size. This problem is known as an impacted wisdom tooth, where the crowding of the other teeth leaves little or no space for the wisdom tooth to make a proper appearance thereby causing pain and discomfort. </p>
<p style="text-align: left;">If the wisdom tooth gets trapped in the jawbone or the gum, not only can it cause a lot of pain, but it can start eating away at the roots of other teeth nearby causing dental damage. There are several degrees of impacted wisdom teeth, depending on where the teeth are placed within the jaw. </p>
